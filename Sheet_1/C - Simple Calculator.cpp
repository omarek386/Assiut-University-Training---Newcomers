#include <iostream>
#include <string>
using namespace std;
int main()
{
    long long X, Y;
    long long mult;
    cin >> X >> Y;
    mult = X * Y;
    cout << X << " + " << Y << " = " << X + Y << endl;
    cout << X << " * " << Y << " = " << mult << endl;
    cout << X << " - " << Y << " = " << X - Y << endl;
}